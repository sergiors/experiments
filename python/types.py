#!/usr/bin/python

# str
print(type('hello'))

# int
print(type(1))

# float
print(type(1.0))

# list
print(type([1, 2, 3, 4])) 

# tuple (immutable list)
print(type((1, 2, 3, 4)))

# dict
print(type({'x': 1, 'y': 1}))
print(type(dict(x=1, y=1)))
print(type({}))

# set
print(type({1, 2, 3}))
print(type(set([1, 2, 3])))
